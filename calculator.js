function addCalculator(a, b) {
  return a + b;
}


function subCalculator(a,b) {
  return a-b;
}

function multiCalculator(a,b) {
  return a*b;
}

function divCalculator(a,b) {
  return a/b;
}

function addMoreCalculator(a,b,c) {
  return a+b+c;
}








module.exports =  {addCalculator, subCalculator, multiCalculator, divCalculator, addMoreCalculator};


